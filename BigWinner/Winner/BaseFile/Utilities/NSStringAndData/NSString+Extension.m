+ (BOOL)isBlankString:(NSString *)str{
    NSString *strss = [NSString stringWithFormat:@"%@",str];
    if (strss == nil || strss == NULL){
        return YES;
    }
    if ([strss isKindOfClass:[NSNull class]]) {
        return YES;
    }
    if ([strss isEqual:@"<null>"]) {
        return YES;
    }
    if ([strss isEqual:@"[null]"]) {
        return YES;
    }
    if ([strss isEqual:@"(null)"]) {
        return YES;
    }
    if ([strss isEqual:@"null"]) {
        return YES;
    }
    if ([[strss stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceCharacterSet]] length]==0) {
        return YES;
    }
    return NO;
}
